static void serial_prepare_write()
{
  DELAY_US(1);
  HAL_GPIO_WritePin(SPI2_CS_GPIO_Port, SPI2_CS_Pin, GPIO_PIN_RESET);
}

static void serial_end_write()
{
  HAL_GPIO_WritePin(SPI2_CS_GPIO_Port, SPI2_CS_Pin, GPIO_PIN_SET);
  DELAY_US(1);
}

static void serial_write_byte(uint8_t byte)
{
  HAL_SPI_Transmit(&hspi2, &byte, 1, 2);
}

static void serial_write(uint8_t* data, uint8_t size)
{
  serial_prepare_write();
  for (uint8_t i = 0; i < size; i++)
    serial_write_byte(data[i]);
  serial_end_write();
}

void PT6315_Write_Cmd(uint8_t cmd)
{
  serial_write(&cmd, sizeof(cmd));
}

void PT6315_Write_Byte(uint8_t addr, uint8_t byte)
{
  uint8_t data[] = { 0xC0 | (addr & 0x3F), byte };
  serial_write(data, sizeof(data));
}

// up to 36 bytes
void PT6315_Write_Data(uint8_t addr, uint8_t* data, uint8_t size)
{
  serial_prepare_write();
  uint8_t cmd3 = 0xC0 | (addr & 0x3F);
  serial_write_byte(cmd3);
  for (uint8_t i = 0; i < size; i++)
    serial_write_byte(data[i]);
  serial_end_write();
}

void PT6315_Init()
{
  uint8_t data[] = {
      0xC0 | 0x00, 0x0F, 0x00,
      0x14 | 0x03, 0xA0, 0x00,
      0x48 | 0x00, 0x44, 0x00,
      0xC0 | 0x00, 0x8F, 0x00,
      0xE0 | 0x00, 0x1C, 0x00,
      0xC4 | 0x00, 0x8C, 0x00,
      0x00 | 0x00, 0x00, 0x00,
      0x00 | 0x00, 0x00, 0x00,
      0x00 | 0x00, 0x00, 0x00,
      0x00 | 0x00, 0x00, 0x00,
      0x00 | 0x00, 0x00, 0x00,
      0x00 | 0x00, 0x00, 0x00,
  };
  PT6315_Write_Cmd(0x40 | 0x00);		// cmd2, 0x08 - test mode
  PT6315_Write_Data(0x00, data, sizeof(data));	// set display data
  PT6315_Write_Cmd(0x00 | 0x08);		// cmd1, 12 digits, 16 segments
  PT6315_Write_Cmd(0x80 | 0x08);		// cmd4, 0x08 - display ON
}
