// to get printf() work
int __io_putchar(int ch)
{
  if (ch == '\n') {
    static uint8_t cr = '\r';
    HAL_UART_Transmit(&huart1, &cr, 1, 1);
  }
  HAL_UART_Transmit(&huart1, (uint8_t*)&ch, 1, 1);
  return ch;
}

void max7219_write_reg_all(uint8_t reg, uint8_t value)
{
  uint8_t tx_data[2] = { reg, value };
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_RESET);
  for (int i = 0; i < 4; i++)
    HAL_SPI_Transmit(&hspi1, tx_data, sizeof(tx_data), 2);
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_SET);
}

void max7219_clear_all()
{
  for (int i = 0; i < 8; i++) {
    max7219_write_reg_all(i + 1, 0x00);
  }
}

void max7219_init_all()
{
  max7219_clear_all();
  max7219_write_reg_all(SHUTDOWN_REG, 0);
  max7219_write_reg_all(DISPLAY_TEST_REG, 0);
  max7219_write_reg_all(SCAN_LIMIT_REG, 7);
  max7219_write_reg_all(DECODE_MODE_REG, 0);
  max7219_write_reg_all(SHUTDOWN_REG, 1);
}

void max7219_write_frame(const uint64_t* frame, uint8_t cnt)
{
  for (uint8_t i = 0; i < 8; i++) {         // rows
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_RESET);
    for (uint8_t s = 0; s < cnt; s++) {       // segments
      uint8_t* rows = (uint8_t*)(&frame[cnt-s-1]);
      uint8_t tx_data[2] = { i+1, rows[i] };
      HAL_SPI_Transmit(&hspi1, tx_data, sizeof(tx_data), 2);
    }
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_SET);
  }
}

